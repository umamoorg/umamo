/**
 * Shorthand for an opaque 8-bit-per-channel color, used by the cursor table below for the handful of
 * near-black and accent colors the source art carries alongside plain white and black.
 *
 * @param Int red   Red channel, 0-255.
 * @param Int green Green channel, 0-255.
 * @param Int blue  Blue channel, 0-255.
 * @return Color The opaque color.
 */
private fun rgb(red: Int, green: Int, blue: Int): Color = Color(red = red, green = green, blue = blue)

/**
 * Builds one cursor layer from its SVG path data, applying the fill rule at parse time so the shape's
 * holes render correctly.
 *
 * @param String       pathData SVG "d" attribute contents; may hold several "M..." subpaths.
 * @param Color        fill     Solid fill color, or null for an unfilled (stroke-only) path.
 * @param CursorStroke stroke   Outline description, or null for an unstroked (fill-only) path.
 * @param Boolean      evenOdd  True to fill by the even-odd rule (holes); false for non-zero winding.
 * @return CursorLayer The layer ready to compose into a [cursor].
 */
private fun layer(
	pathData: String,
	fill: Color? = null,
	stroke: CursorStroke? = null,
	evenOdd: Boolean = false,
): CursorLayer {
	val path = PathParser().parsePathString(pathData).toPath()
	if (evenOdd) {
		path.fillType = PathFillType.EvenOdd
	}
	return CursorLayer(path = path, fill = fill, stroke = stroke)
}

/**
 * Bundles a cursor's layers with the viewport they are authored in and its hotspot, measuring the tight
 * bounds of the painted art from the layer paths so drawing can anchor to the real art, not the viewport.
 *
 * @param Float         viewportWidth  Width of the authored coordinate box.
 * @param Float         viewportHeight Height of the authored coordinate box.
 * @param CursorLayer   layers         The paint layers, first to last.
 * @param CursorHotspot hotspot        The point that tracks the pointer; defaults to the art's center.
 * @return UmamoCursor The assembled cursor.
 */
private fun cursor(
	viewportWidth: Float,
	viewportHeight: Float,
	vararg layers: CursorLayer,
	hotspot: CursorHotspot = CursorHotspot.MiddleCenter,
): UmamoCursor {
	var left = Float.MAX_VALUE
	var top = Float.MAX_VALUE
	var right = -Float.MAX_VALUE
	var bottom = -Float.MAX_VALUE
	for (layer in layers) {
		val layerBounds = layer.path.getBounds()
		left = min(left, layerBounds.left)
		top = min(top, layerBounds.top)
		right = max(right, layerBounds.right)
		bottom = max(bottom, layerBounds.bottom)
	}
	return UmamoCursor(
		layers = layers.toList(),
		viewportWidth = viewportWidth,
		viewportHeight = viewportHeight,
		bounds = Rect(left = left, top = top, right = right, bottom = bottom),
		hotspot = hotspot,
	)
}

