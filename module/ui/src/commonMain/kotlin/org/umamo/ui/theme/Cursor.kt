/**
 * The stroked outline of one cursor layer: its color, line weight, and cap/join.  Cursor art is drawn
 * with a white body under a black outline so it stays legible over any canvas color, so a layer carries
 * its own concrete colors rather than deriving them from a single caller tint the way the icon set does.
 *
 * @param Color      color       The stroke color.
 * @param Float      strokeWidth Line weight in the cursor's own viewport units (it scales with the art).
 * @param StrokeCap  cap         End-cap style; round for the pen and finger-separator strokes, else butt.
 * @param StrokeJoin join        Corner style; round throughout the set to match the baked source art.
 */
data class CursorStroke(
	val color: Color,
	val strokeWidth: Float,
	val cap: StrokeCap = StrokeCap.Butt,
	val join: StrokeJoin = StrokeJoin.Round,
)

/**
 * One drawn element of a cursor: a vector path that may be filled, outlined, or both.  The classic
 * cursor silhouette is a single path filled white and then stroked black, which is why fill and stroke
 * are independent and optional on the same path instead of being split across two layers.
 *
 * @param Path         path   The parsed vector outline, with its fill rule already applied.
 * @param Color        fill   Solid fill color, or null to leave the path unfilled.
 * @param CursorStroke stroke Outline description, or null to leave the path unstroked.
 */
data class CursorLayer(
	val path: Path,
	val fill: Color? = null,
	val stroke: CursorStroke? = null,
)

/**
 * A cursor's hotspot as one point of a nine-point grid over its painted art: a horizontal anchor (left,
 * center, or right) crossed with a vertical one (top, middle, or bottom).  This is the point that tracks
 * the pointer — the arrow's tip, a crosshair's center, an eyedropper's nib — so drawCursor places it
 * exactly on the anchor the caller gives and the caller never offsets for it.  The fractions address the
 * art bounds, not the authored viewport, so a corner lands on the real tip.  A coarse grid is a
 * deliberately sensible default; a cursor that needs a pixel-exact hotspot can be refined later.
 *
 * @param Float fractionX Horizontal position within the art bounds (0 = left, 0.5 = center, 1 = right).
 * @param Float fractionY Vertical position within the art bounds (0 = top, 0.5 = middle, 1 = bottom).
 */
enum class CursorHotspot(val fractionX: Float, val fractionY: Float) {
	TopLeft(0f, 0f),
	TopCenter(0.5f, 0f),
	TopRight(1f, 0f),
	MiddleLeft(0f, 0.5f),
	MiddleCenter(0.5f, 0.5f),
	MiddleRight(1f, 0.5f),
	BottomLeft(0f, 1f),
	BottomCenter(0.5f, 1f),
	BottomRight(1f, 1f),
}

/**
 * A single themed vector cursor: its paint layers in draw order, the viewport their coordinates are
 * authored in, the tight bounds of the painted art, and its hotspot.  Unlike icons the viewport is not
 * square (a north-south arrow is tall, an east-west arrow is wide) and the art rarely fills it to the
 * edge, so the hotspot and the draw size are taken from [bounds] (the union of the layer paths) rather
 * than the viewport; that way a corner hotspot lands on the real tip, not on empty authored margin.
 *
 * @param List<CursorLayer> layers         The paint layers, drawn first to last.
 * @param Float             viewportWidth  Width of the coordinate box the paths are authored in.
 * @param Float             viewportHeight Height of the coordinate box the paths are authored in.
 * @param Rect              bounds         Tight bounding box of the painted art, in viewport units.
 * @param CursorHotspot     hotspot        The point that tracks the pointer (see [CursorHotspot]).
 */
data class UmamoCursor(
	val layers: List<CursorLayer>,
	val viewportWidth: Float,
	val viewportHeight: Float,
	val bounds: Rect,
	val hotspot: CursorHotspot = CursorHotspot.MiddleCenter,
)

