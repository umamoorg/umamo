/**
 * Draws [cursor] with its hotspot placed exactly on [anchor] (typically the pointer position), scaled so
 * the cursor's longer side spans [pixelSize].  Because the art is positioned by its hotspot the caller
 * passes the pointer location and a size and never offsets for the tip or center itself.  Draw into a
 * surface that surrounds [anchor] (an overlay over the viewport), not a tight cursor-sized box: a
 * non-corner hotspot places art on every side of [anchor].  Colors come baked into the layers, so unlike
 * drawIcon there is no caller tint.
 *
 * @param UmamoCursor cursor    The cursor to paint.
 * @param Offset      anchor    Where the cursor's hotspot lands, in this DrawScope's coordinates.
 * @param Float       pixelSize Target length of the cursor's longer side, in pixels.
 */
fun DrawScope.drawCursor(cursor: UmamoCursor, anchor: Offset, pixelSize: Float = 16f) {
	val bounds = cursor.bounds
	// Scale so the art's longer side spans pixelSize, keeping the authored aspect ratio.
	val drawScale = pixelSize / max(bounds.width, bounds.height)
	// The hotspot is a point within the art bounds; shift the origin so its scaled position lands on the
	// anchor, so the art draws around the anchor and the caller never offsets for the tip or center.
	val hotspotX = bounds.left + cursor.hotspot.fractionX * bounds.width
	val hotspotY = bounds.top + cursor.hotspot.fractionY * bounds.height
	val originX = anchor.x - hotspotX * drawScale
	val originY = anchor.y - hotspotY * drawScale
	withTransform({
		translate(left = originX, top = originY)
		scale(scaleX = drawScale, scaleY = drawScale, pivot = Offset.Zero)
	}) {
		for (layer in cursor.layers) {
			val fillColor = layer.fill
			if (fillColor != null) {
				drawPath(path = layer.path, color = fillColor, style = Fill)
			}
			val stroke = layer.stroke
			if (stroke != null) {
				drawPath(
					path = layer.path,
					color = stroke.color,
					style = Stroke(width = stroke.strokeWidth, cap = stroke.cap, join = stroke.join),
				)
			}
		}
	}
}
